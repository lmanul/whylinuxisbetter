<h2>Installér Linux</h2>

<p>Okay, så du har besluttet dig for at skifte til Linux? Du vil ikke fortryde det!</p>

<p>Hvis du gerne vil beholde Windows på din computer (hvilket jeg anbefaler i tilfælde af at du skifter mening, eller i tilfælde af at du finder ting du kun kan gøre under Windows), så skal Windows dele diskplads med Linux. Hver gang du starter computeren skal du herefter vælge om du vil boote ind iWindows eller Linux. </p>

<p>Du bør først <b>gemme al din data</b>. Dette er utroligt vigtigt. Man ved aldrig hvad der kan gå galt. Det er ikke løgn. Gem dine filer, find en blank CD, blank DVD, en anden harddisk, hvad som helst, gem dine ting.</p>

<p>Først bør du defragmentere din harddisk. Lige nu er dine data spredt ud over hele din harddisk (Windows magter ikke at gøre dette ordentligt, den slags er Linux meget bedre til). Efter defragmenteringen (dette kan tage et par timer, afhængigt af størrelsen på din harddisk) vil alle dine data være samlet i begyndelsen af din harddisk (tænk på det som en lang rulle tape med en begyndelse og en slutning). Hvis dine data er samlet dér vil Linux ikke have nogen problemer med at splitte (virtuelt, selvfølgelig :) ) din harddisk op, og installerer sig selv på de nye dele. </p>

<p>Okay, så er du klar, nu skal du bare vælge hvilken Linux-distribution du vil have. Linux kommer i mange forskellige varianter, lavet af mange forskellige firmaer, med forskellige sæt software, forskelligt udséende, etc. De er alle sammen Linux, men i forskellige "fortolkninger". Det er ikke et åbenlyst valg, og her kan jeg ikke helt hjælpe dig (faktisk er der mange der diskuterer om hvorfor lige netop deres version er bedst). Du kan derimod tage et kig på de 4 mest populære distributioner af Linux, rangeret af <a href="http://www.distrowatch.com">distrowatch.com</a> :</p>

