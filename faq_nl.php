<li><p><b>V: Hey, was jij niet een van de <a href="http://code.google.com/summerofcode.html">Google's Summer of Code</a>-mensen, een van de 400-uit-9000 gekozen hackers?</b></p>

<p>A: Ja. Maar ik had geluk: Ik verzond 8000 van de 9000 projectenvoorstellen, en één ervan werd goedgekeurd. Ok, ik had twee keer geluk (2005, 2006). Volgend jaar ben ik van plan mijn automatische project generator Open Source te maken.</p>

<li><p><b><a name="help">V: Hallo. Ik krijg XYZ niet aan de praat op Linux. Kan u mij helpen?</a></b></p>

<p>A: Het spijt me, maar mijn e-mailadres moet niet geziend worden als een ongelimiteerd ondersteuningsadres. Zoals veel andere mensen heb ik veel dingen te doen en hoewel ik graag help wanneer ik kan en hiervoor tijd heb, ben ik er zeker van dat er veel mensen zijn in forums en IRC-kanalen nog liever <a href="http://www.whylinuxisbetter.net/items/help/index.php">uw vragen beantwoorden</a>!
En een <a href="http://www.google.com">Google</a>-zoekopdracht is altijd een goede manier om te starten.</p></li>

