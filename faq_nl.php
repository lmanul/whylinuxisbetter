<h2>Een kleine FAQ voor Linux-gebruikers</h2>

<p>Ik start met het zeggen dat, wat u waarschijnlijk al gemerkt had, deze website niet bedoeld is voor u :) Het is bedoeld voor mensen die vanuit Windows (of Mac) willen weten wat Linux is en of het geschikt is voor hen. U vindt hier waarschijnlijk geen nieuwe informatie en kan best Slashdot blijven lezen. :-p</p>

<ul>

<li><p><b>V: Linux is geen volledig besturingssysteem zoals Windows. Waarom vertel je de lezers niet het hele verhaal?</b></p>

<p>A: Sint Linus vergeef me :) Deze vereenvoudiging doe ik met opzet. Als ik begin uit te leggen dat Linux slechts de kernel is en dat verschillende aanbieders Linux uitrusten met software naar hun keus en zo een Linuxdistributie maken is de helft van de lezers al lang vertrokken. Dit is geen website voor mensen die Linux al als hun besturingssysteem gebruiken (tenzij ze willen helpen met het verbeteren van de website of mijn slechte Engels te verbeteren, ik heb het nodig!). Voor dezelfde reden, en voor de eenvoud, spreek ik niet over de verschillende BSD's en het feit dat veel <i>grote</i> servers BSD draaien en geen Linux.
</p>
 
<li><p><b>V: Waarom zegt u dat het ene besturingssysteem beter is dan het andere? Het hangt af van wat gebruikers willen.</b></p>

<p>A: Natuurlijk, daar ben ik het volkomen mee eens. Maar laat ik duidelijk zijn over wat ik hier wil doen: 1) Laat Windows/Mac gebruikers weten dat er iets bestaat dat Linux heet. 2) Vertel een paar redenen waarom Linux een goed systeem is, en waarom het meestal een goede keus is. Natuurlijk, het adres van de website is minder gecompliceerd dan dit, maar zou ik whylinuxisbetterinmanycases.net(waaromlinuxinveelgevallenbeteris.net) moeten kopen?</p>

<p>Ik ben geen Linux-deskundige, geen sekteleider en ik wil niet bekeren: ik ben alleen een erg gelukkige Linux-gebruiker en -ontwikkelaar die dat met andere mensen wil delen.</p>

<li><p><a name="gnu"><b>V: Het is GNU/Linux, niet Linux, idioot!</b></a></p>

<p>A: In een website voor gevorderde gebruikers van Linux zouden dit soort details van belang zijn. Maar houd de dingen een beetje eenvoudig. De mensen hoorden van "Linux", en niet van "GNU/Linux", en dit soort discussies weerhouden "normale" mensen ervan te begrijpen waar het nou allemaal om gaat. Om dezelfde reden schrijf ik bijvoorbeeld "OpenOffice" in plaats van "OpenOffice.org".</p>

<p>Oja, deze vraag is eigenlijk helemaal geen vraag, omdat het geen vraagteken heeft. Maar toch, omdat ik zowel vragen als antwoorden schrijf. O god, ik ben echt dom.</p>

<li><p><b>V: Uw Engels is slecht.</b></p>

<p>A: Ja, ik weet het. En ik werk eraan. Volgende vraag graag.</p>

<li><p><b>V: Ik wil graag meehelpen met het vertalen van de website in mijn taal, hoe doe ik dat?</b></p>

<p>A: Graag! Voel je vrij om mij te e-mailen naar <a href="mailto:ideas@whylinuxisbetter.net">ideas@whylinuxisbetter.net</a>. En als Engels uw moedertaal is, e-mail a.u.b. alle fouten die u gevonden hebt naar mij, zodat ik de fouten kan herstellen.</p>

<li><p><b>V: U hebt het vooral over GNOME en Ubuntu, hebt u iets tegen KDE en andere distributies?</b></p>

<p>A: Natuurlijk niet. Ik gebruik veel KDE-programma's (en als al die inspanningen waarmee wordt geruzied over of GNOME beter is of KDE in het verbeteren van compatibiliteit tussen KDE en GNOME zou worden gestopt, zou ik heel blij zijn). En op mijn screenshots is meestal Ubuntu te zien omdat ik die gebruik en er voor ontwikkel. Het is onnodig te zeggen, dat als u betere screenshots heeft deze naar mij mag mailen. U zou kunnen opmerken dat ik een stuk heb over AmaroK, aarzel niet om mij suggesties te sturen over andere KDE-programma's (en GNOME!).</p>

<li><p><b>V: Wat weet u eigenlijk over Linux?</b></p>

<p>A: Waarschijnlijk niet zo veel als ik zou moeten! Ik ben een lid van Ubuntu, gebruik sinds ongeveer 1998 Linux, schreef een paar pocketboeken (in het Frans) over wat opensourcesoftware (Firefox, Linux, enz.). Maar dat probeer ik op te lossen, ik heb net het boek "Understand the Linux kernel" besteld (wanneer u dit leest, weet ik misschien al alles over Linux wat er te weten valt en heb ik een eigen versie gemaakt, Manux -- oké misschien ook wel niet :-p).</p>

<li><p><a name="gaming"><b>V: U zei dat gamers niet moeten overstappen naar Linux. Maar kent u Wine, CrossOver, PlayOnLinux en Steam niet? Deze werken voor een groeiend aantal Windows-spellen.</b></a></p>
<p>A: Ik ben akkoord dat verschillende software-oplossingen het makelijker maken om Windows-programma's te draaien op Linux dan het voorheen was. Dit is een goede oplossing voor gelegenheidsgamers. Merk echter op dat ik de term hardcore gamers gebruik, diegene die de meest recente games kopen en hun processors (CPU's) overklokken om betere prestaties te bekomen; ik betwijfel sterk dat deze gebruikers dit soort compromis willen maken.</p>

<p>Als u nog steeds niet overtuigd bent, probeer dan volgend denkexperiment.
Stel u voor dat uw beste vriend een hardcore gamer is. Hij of zij speelt
verschillende uren per dag, elke dag, houdt zijn/haar computer bijgewerkt
met de meeste recente hardware, zodat spellen op het hoogste detailniveau en
met de hoogste snelheid gespeeld kunnen worden. Hij/zij neemt deel in alle
vroege bètaversies en heeft abonnementen voor verschillende massively online
multiplayer games (MOMPG).
Stel u voor dat deze beste vriend van u overweegt over te stappen naar een
ander besturingssysteem, en hij/zij vraagt raad aan u, met de vraag of Linux
een mogelijke keuze is voor hem/haar. Zou u zeggen dat Linux een goede keuze
is? Momenteel zou ik zeggen dat het (nog) geen goede keuze is.</p>
</li>

