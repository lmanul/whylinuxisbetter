<h2>Donanım</h2>

<p>Çoğu donanım parçası Linux ile birlikte düzgün bir şekilde çalışır. Ancak, çok yeni donanımlar (birkaç aylık) bazı durumlarda henüz desteklenmemiş olabiliyor. Yazılım üreticileri gibi, çoğu donanım üreticisi de Linux pazarını hala çok küçük olarak değerlendiriyor ve çıkardıkları donanımlar için Linux sürücüleri sağlama zahmetine girmiyorlar. Linux geliştiricileri bu konu üzerinde çalışıyorlar ve herbir donanım parçasının tıkır tıkır çalışması için çabalıyorlar ancak bu biraz zaman alıyor.</p>

<p>Bir donanımın desteklenip desteklenmediğini görmenin en iyi ve doğrudan yolu Linux'u bir Çalışan CD ile denemektir ("Windows'a dokunmadan Linux'u deneyin" kısmına göz atın).</p>

