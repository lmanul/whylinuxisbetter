<h2>Hardware</h2>

<p>La mayoría de piezas de hardware trabaja bien en Linux. Sin embargo, algunas veces un hardware muy reciente (de unos pocos meses de antigüedad) no estará soportado por el momento. Muchos vendedores de programas todavía consideran que el mercado de Linux es muy pequeño y no se molestan en crear controladores de su hardware para Linux. Los programadores de Linux trabajan en eso y hacen un trabajo excelente para dejar cada pieza de hardware trabajando como la seda, pero esto toma un poco de tiempo.</p>

<p>La mejor manera y la más directa de saber si tu hardware está soportado, es hacer funcionar Linux desde un Live CD (ver la sección "Prueba Linux sin tocar Windows").</p>

