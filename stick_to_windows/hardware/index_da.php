<h2>Hardware</h2>

<p>Det meste hardware virker fint med Linux. Meget ny hardware (et par måneder gammelt) er dog som regel ikke supporteret. Ligesom software-producenterne anser hardware-producenterne stadigvæk Linux som et forholdsvist lille marked, og kærer sig ikke om at lave Linux-drivere til deres hardware. Linux-udviklerne arbejder dog på dette, og gør et fortræffeligt arbejde med at få al den forskellige hardware til at virke under Linux, men det tager alligevel en smule tid at få lavet.</p>

<p>Den bedste og mest ligetil måde at tjekke om din hardware er supporteret under Linux er, at prøve at køre en Live-CD (se "Prøv Linux uden at røre ved Windows"-sektionen).</p>

