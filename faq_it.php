<h2>Alcune FAQ per geek di Linux</h2>

<p>Ok, posso cominciare da questo: come avrai già notato, questo 
sito non è proprio adatto a te! :) E' indicato per utenti Windows 
(o Mac) che cercano di capire cosa sia Linux, com'è fatto, e 
controllare se contenga qualcosa di bello per loro. Probabilmente 
non troverai nessuna informazione nuova qui, e preferirai continuare 
a leggere slashdot! :-p</p>

<ul>

<li><p><b>D: Linux non è un sistema operativo completo come Windows, è 
solo il kernel. Perché far credere agli utenti che non lo sia?</b></p> 
<p>R: Santo Linus, perdonami! :) Si tratta di semplificazione volontaria, 
e diversi sviluppatori scelgono di distribuire Linux con la loro scelta 
di software per creare una "distribuzione Linux", ecc.; metà dell'audience 
se ne sarà già andata. Questo non è proprio un sito per gente che usa già 
Linux come il loro OS principale (a meno che non vogliano aiutarci a 
rendere il sito migliore -- e a correggere il mio Inglese, ne ho bisogno!).</p> </li>

<li><p><b>D: Perché dici che un OS sia meglio di un altro? Dipende 
da cosa la gente voglia farci.</b></p>
<p>R: Certo che sì, sono d'accordo. Ma chiariamo prima cosa sto 
cercando di fare qui: 1) Far sapere agli utenti Windows/Mac OS X che
esiste qualcos'altro. 2) Dire alcune ragioni per le quali Linux sia 
un sistema veramente bello e perché spesso sia un'ottima scelta. 
Ovviamente, il nome del dominio è molto più corto, ma... Ehi! Avrei 
dovuto acquistare <tt>whylinuxisbetterinmanycases.net</tt>?</p>

<p>Non sono un esperto di Linux, né un leader del settore, e non voglio 
evangelizzarlo: sono solo un utente estremamente felice e sviluppatore 
di Linux che cerca di farlo scoprire a quante più persone possibile.</p>

</li>

<li><p><b>D: È GNU/Linux, non Linux, razza di idiota!</b></p>
<p>R: In un sito web per utenti Linux avanzati questi dettagli importerebbero, 
ma semplifichiamo le cose. La gente sente parlare di "Linux", non di 
"GNU/Linux", e questo tipo di dibattito geekoso è probabilmente uno dei 
motivi che non permettono a persone "normali" di capire di cosa si tratti. 
Per questa stessa ragione, scrivo "OpenOffice" invece di "OpenOffice.org" 
e così via.</p>

<p>E, comunque, questa "D:" non è una domanda, dato che non ha un 
punto interrogativo. Però, dato che scrivo sia le domande che le 
risposte... Oh, mamma! Sono veramente stupido.</p>

</li>

<li><p><b>D: Ehi, tu! Il tuo Inglese fa veramente schifo!</b></p>
<p>R: Sì, lo so. Ci sto lavorando. Passiamo appresso, per favore!</p>
</li>

<li><p><b>D: Vorrei contribuire traducendo il sito nella mia lingua, sei interessato?</b></p>
<p>R: Assolutamente! Sentiti libero di inviarmi un'email a <a 
href="mailto:ideas@whylinuxisbetter.net">ideas@whylinuxisbetter.net</a>. 
E se sei un inglese madrelingua, per favore riportami le prove del mio povero inglese allo stesso indirizzo, in modo che possa correggere i miei errori.</p>
</li>

<li><p><b>D: Sembri concentrarti su GNOME e Ubuntu. Hai avuto brutte 
impressioni di KDE e delle altre distribuzioni?</b></p> 
<p>R: Certo che no. Adoro KDE e uso molte sue applicazioni (se tutta la 
volontà di mettere GNOME contro i troll di KDE possa essere investita nell' 
intregrare bene le applicazioni per KDE in GNOME e viceversa, sarei molto 
contento). E i miei screenshot mostrano spesso Ubuntu perché è la distribuzione 
che uso e sulla quale sviluppo. Senza che ve lo dica, mandatemi altri screenshot. 
Avrete notato che ne ho uno di AmaroK, non esitate a mandarmi altri suggerimenti 
per altre applicazioni K (e GNOME!).</p> </li>

<li><p><b>D: Che cavolo ne sai tu di Linux, comunque?</b></p>
<p>R: Probabilmente non tanto quanto dovrei! Sono un membro di Ubuntu, uso 
Linux dal 1998, ho scritto alcuni libri tascabili (in Francese) su alcuni 
software Open Source (Firefox, Linux, ecc.). Ora sto cercando di migliorarmi, 
ho ordinato il libro "Understand the Linux kernel" (dal momento che leggi questo 
lo dovrei aver finito e capito tutto ciò che c'è da sapere su Linux per crearne 
una mia versione, che chiamerò senza dubbio "Manux" -- ok, forse no! :-p).</p> </li>

<a name="gaming"><li><p><b>D: Hai scritto che gli hardcore gamer non dovrebbero passare a Linux, ma non conosci Wine, Cedega, Steam? Funzionano bene per un numero sempre maggiore di giochi per Windows.</b></p><p>R: Sono d'accordo sul fatto che varie soluzioni software fanno funzionare i giochi Windows su Linux molto più facilmente di come sarebbe. È un'ottima soluzione per i casual gamer. Tuttavia, nota che ho usato l'espressione "hardcore gamer", coloro che acquistano i giochi più recenti e "overcloccano" la loro CPU per avere prestazioni migliori; dubito che questi siano disposti ad accettare quel tipo di compromesso.</p></li>

<p><a name="distros"><li><b>D: Per favore aggiungi la distribuzione Linux XYZ alla tua pagina sull'installazione di Linux.</b></a></p>
<p>R: Ci sono tante, tante distribuzioni in giro, e qualsiasi sottoinsieme limitato non potrebbe soddisfare tutti. Per le persone che vogliono provare Linux, è già abbastanza confusionario il fatto che ce ne siano varie "versioni/gusti". Ho scelto di parlare di 4 di esse che sono state e sono tuttora tra le più popolari, penso sia una scelta ragionevole.</p></li>

<li><p><b>D: Ma tu non sei quel tipo strambo di <a 
href="http://www.manucornet.net/pcjacking">PCJacking</a>?</b></p>
<p>R: Ops, già.</p>
</li>

<li><p><b>D: Ma tu non sei quel tipo strambo di <a 
href="http://www.manucornet.net/Informatique/Gimp_Texturize.php">GIMP-Texturize</a> 
?</b></p>
<p>R: Certo, mi hai preso! :) Ma <a 
href="http://jb.rouquier.free.fr/">Jean-Baptiste Rouquier</a> ha  
fatto metà del lavoro. Ok, di più. Huh, prossima domanda.</p>
</li>

<li><p><b>D: Hey, non sei uno di quei tipi di <a 
href="http://code.google.com/summerofcode.html">Google's Summer of 
code</a>, uno dei 400 hacker scelti su 9000?</b></p>
<p>R: Già. Ma sono stato solo fortunato: ho inviato 8000 proposte per quei 9000 
progetti, e una di quelle è stata accettata. Il prossimo anno renderò open 
source il mio generatore automatico di progetti.</p> </li>

<li><p><b><a name="help">D: Ciao! Non riesco a far funzionare XYZ su Linux. Puoi aiutarmi?</a></b></p>

<p>R: Mi dispiace, ma il mio indirizzo email non dovrebbe essere considerato un centro di supporto illimitato su Linux :-) Come molta altra gente, ho molte cose da fare, e anche se sono felice di aiutare quando posso e ho il tempo necessario, sono sicuro che tutte quelle persone nei forum e nei canali IRC saranno molto disponibili a <a href="http://www.whylinuxisbetter.net/items/help/index.php">rispondere alle tue domande</a>!
E una ricerca su <a href="http://www.google.com">Google</a> è sempre un buon inizio.</p></li>

